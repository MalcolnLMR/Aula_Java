package experimentos_gson;

public class SimpleCoords {
    public String name;
    public String world;
    public double x;
    public double y;
    public double z;

    public SimpleCoords(String name, String world, double x, double y, double z) {
        this.name = name;
        this.world = world;
        this.x = x;
        this.y = y;
        this.z = z;
    }

}
